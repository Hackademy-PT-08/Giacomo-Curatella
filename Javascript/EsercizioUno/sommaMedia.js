let firstNumber = parseInt(prompt("Inseriscri il primo numero"));
let secondNumber = parseInt(prompt("Inseriscri il secondo numero"));
let thirdNumber = parseInt(prompt("Inseriscri il terzo numero"));
let fourthNumber = parseInt(prompt("Inseriscri il quarto numero"));
let fifthNumber = parseInt(prompt("Inseriscri il quinto numero"));

let somma = firstNumber + secondNumber + thirdNumber + fourthNumber + fifthNumber;
console.log(`La somma dei cinque numeri è: ${somma}`);
let media = (firstNumber + secondNumber + thirdNumber + fourthNumber + fifthNumber) / 5;
console.log(`La media dei 5 numeri è: ${media}`);